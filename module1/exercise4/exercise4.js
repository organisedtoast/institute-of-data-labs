function add(a, b) {
return a + b
}

function subtract(a, b) {
return a - b
}

function multiple(a, b) {
return a * b
}

function divide(a, b) {
return a / b
}

console.log(add(10, 5));

console.log(subtract(10, 5));

console.log(multiple(10, 5));

console.log(divide(10, 5));

